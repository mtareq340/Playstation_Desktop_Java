    private void addButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButton2ActionPerformed
        String name=nameTextField.getText();
        String price=priceTextField.getText(); 
        String multi=multiTextField.getText();
        
        boolean textempty=false;
        boolean nameFound=false;
        deviceDao dd = new deviceDao();
        List<Device>d=dd.selectDevice();
        for(int i=0;i<d.size();i++)
        {
        if(d.get(i).getDeviceName().equalsIgnoreCase(name))
        {
        nameFound=true;
        }   
        }
        
                if(nameFound)
        {
               JOptionPane.showMessageDialog(null, "اسم جهاز مستخدم");
        }
        else
        {
          if(name.isEmpty() || price.isEmpty() || multi.isEmpty())
        {
        JOptionPane.showMessageDialog(null, "املى الفورغ");
        }
            else{   
        Device device = new Device( name,price,multi);
        dd.insertDevice(device);
        JOptionPane.showMessageDialog(null, "تم الاضافه");
        tableAction();
        idTextField.setText("");
        nameTextField.setText("");
        priceTextField.setText("");    
        multiTextField.setText("");

        }
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_addButton2ActionPerformed

