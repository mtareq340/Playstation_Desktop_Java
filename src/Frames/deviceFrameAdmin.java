public void tableAction()
    {
        DefaultTableModel model = (DefaultTableModel) deviceTable.getModel();
        model.setRowCount(0);
        deviceDao dd = new deviceDao();
        List<Device>d=dd.selectDevice();
        model =(DefaultTableModel) deviceTable.getModel();
        Object[] row = new Object[4];
        for (int i = 0; i < d.size(); i++) 
        {
         row[0]=d.get(i).getDevicemulti();
         row[1]=d.get(i).getDevicePrice();
         row[2]=d.get(i).getDeviceName();
         row[3]=d.get(i).getDeviceId();
         model.addRow(row);
        }
        }
